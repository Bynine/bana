package main;

import com.badlogic.gdx.Input.Keys;

public class WorldMap {
	// textures for maps
	public boolean keyUp(int keycode) {
		switch(keycode){
		default: break;
		}
		return false;
	}
	public boolean keyDown(int keycode) {
		switch(keycode){
		case Keys.W: break;
		case Keys.A: break;
		case Keys.S: break;
		case Keys.D: break;
		default: break;
		}
		return false;
	}
}
